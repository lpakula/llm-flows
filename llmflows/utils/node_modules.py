"""Resolve node_modules for MCP / Pi extensions on host vs Docker runner."""

import logging
import os
import subprocess
from pathlib import Path

from ..config import SYSTEM_DIR

logger = logging.getLogger("llmflows.node_modules")

_CONTAINER_TOOLS_DIR = Path("/opt/llmflows/tools")
_CONTAINER_NODE_MODULES = _CONTAINER_TOOLS_DIR / "node_modules"
_HOST_NODE_MODULES = SYSTEM_DIR / "node_modules"


def resolve_node_modules() -> Path:
    """Return node_modules appropriate for the current platform/runtime.

    Runner containers mount the host ``~/.llmflows`` directory, which may
    contain macOS-native esbuild binaries.  Use the Linux deps baked into the
    image at ``/opt/llmflows/tools/node_modules`` instead.
    """
    if os.environ.get("LLMFLOWS_RUNNER"):
        tsx = _CONTAINER_NODE_MODULES / ".bin" / "tsx"
        if tsx.exists():
            return _CONTAINER_NODE_MODULES
    return _HOST_NODE_MODULES


def ensure_runner_node_modules() -> None:
    """Install Linux-native MCP deps in runner containers when missing."""
    if not os.environ.get("LLMFLOWS_RUNNER"):
        return
    if not _CONTAINER_TOOLS_DIR.is_dir():
        return
    marker = _CONTAINER_NODE_MODULES / "@modelcontextprotocol" / "sdk"
    if marker.exists():
        return
    logger.info("Installing runner MCP dependencies in %s", _CONTAINER_TOOLS_DIR)
    subprocess.run(
        ["npm", "install", "--prefix", str(_CONTAINER_TOOLS_DIR)],
        check=False,
        timeout=180,
    )
